char	***ft_setdefinitive(char ***tab)
{
	int j;
	int i;

	j = 0;
	i = 0;
	while (i < 104)
	{
		while (j < 104)
		{
			tab[1][i][j] = tab[0][i][j];
			j++;
		}
		j = 0;
		i++;
	}
	return (tab);
}
